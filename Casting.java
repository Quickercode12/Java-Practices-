class HelloWorld {
    public static void main(String[] args){

        // variable with number sum 
        int firstNumber = 10;
        int secondNumber = 100;
        int add = firstNumber + secondNumber;

        System.out.println("Tow number of the sum : "+ add );


        // casting 
        // casting in java occurs automatic
        double decimal = 100;
        System.out.println("casting number print: " + decimal);

        // Narrowing Casting is also happens
        double deci = (double) 102;
        System.out.println("casting number print again: " + deci);
    }
}